    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jdteTakeDate.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Please select a date", "Info", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtblStitchingMaster.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Select an item to take", "Info", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                int i = 0;
                con = new DBConnection().connect();
                con.setAutoCommit(false);
                stmt = con.createStatement();
                selected = jtblStitchingMaster.getSelectedRows();
                for (int j = 0; j < selected.length; j++) {
                    if (!jtblStitchingMaster.getValueAt(selected[j], 8).equals("C")) {
                        query = "update trn_stitching set date_recieved='" + new SimpleDateFormat("yyyy-MM-dd").format(jdteTakeDate.getDate()) + "', date_updated='" + today + "',flag='I',status ='C' where sl_no = '" + jtblStitchingMaster.getValueAt(selected[j], 0) + "' ";
                        i = stmt.executeUpdate(query);
                    }
                }
                if (i > 0) {
                    JOptionPane.showMessageDialog(this, "Update Successfull!", "Info", JOptionPane.INFORMATION_MESSAGE);
                    con.commit();
                    jButton1ActionPerformed(evt);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            } finally {
                try {
                    if (!con.isClosed()) {
                        con.close();
                    }
                    if (!stmt.isClosed()) {
                        stmt.isClosed();
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed
    private void populatetable() {
        try {
            rowcount = model1.getRowCount();
            for (int i = 0; i < rowcount; i++) {
                model1.removeRow(0);
            }
            rowcount = jtblItem.getRowCount();
            for (int i = 0; i < rowcount; i++) {
                model2.removeRow(0);
            }
            tailrno = jcmbStitchingMaster.getSelectedItem().toString().split("   ");
            con = new DBConnection().connect();
            stmt = con.createStatement();
            if (jcmbStatus.getSelectedItem().equals("Pending")) {
                if (jdteDate.getDate() == null) {
                    query = "select a.sl_no,b.order_no,b.student_name,e.tailor_name,d.item_name,a.qty,a.date_given,a.date_recieved,a.status from trn_stitching a left join mst_student b on a.stsl_no = b.stsl_no left join mst_item d on a.item_no = d.item_no left join mst_tailor e on a.tailor_no = e.tailor_no where a.tailor_no = '" + tailrno[0] + "'  and a.status='P' and a.flag!='D'  and b.flag!='D'  and d.flag!='D'  and e.flag!='D'";
                } else {
                    query = "select a.sl_no,b.order_no,b.student_name,e.tailor_name,d.item_name,a.qty,a.date_given,a.date_recieved,a.status from trn_stitching a left join mst_student b on a.stsl_no = b.stsl_no left join mst_item d on a.item_no = d.item_no left join mst_tailor e on a.tailor_no = e.tailor_no where a.tailor_no = '" + tailrno[0] + "'  and a.date_given = '" + new SimpleDateFormat("yyyy-MM-dd").format(jdteDate.getDate()) + "' and a.status='P' and a.flag!='D'  and b.flag!='D'  and d.flag!='D'  and e.flag!='D'";
                    populateitem("select d.item_name,sum(a.qty) from trn_stitching a left join mst_student b on a.stsl_no = b.stsl_no left join mst_item d on a.item_no = d.item_no left join mst_tailor e on a.tailor_no = e.tailor_no where a.tailor_no = '" + tailrno[0] + "'  and a.date_given = '" + new SimpleDateFormat("yyyy-MM-dd").format(jdteDate.getDate()) + "' and a.status='P' and a.flag!='D'  and b.flag!='D'  and d.flag!='D'  and e.flag!='D' group by a.item_no");
                }
            } else if (jcmbStatus.getSelectedItem().equals("Completed")) {
                if (jdteDate.getDate() == null) {
                    query = "select a.sl_no,b.order_no,b.student_name,e.tailor_name,d.item_name,a.qty,a.date_given,a.date_recieved,a.status from trn_stitching a left join mst_student b on a.stsl_no = b.stsl_no left join mst_item d on a.item_no = d.item_no left join mst_tailor e on a.tailor_no = e.tailor_no where a.tailor_no = '" + tailrno[0] + "'  and a.status='C' and a.flag!='D'  and b.flag!='D'  and d.flag!='D'  and e.flag!='D'";
                } else {
                    query = "select a.sl_no,b.order_no,b.student_name,e.tailor_name,d.item_name,a.qty,a.date_given,a.date_recieved,a.status from trn_stitching a left join mst_student b on a.stsl_no = b.stsl_no left join mst_item d on a.item_no = d.item_no left join mst_tailor e on a.tailor_no = e.tailor_no where a.tailor_no = '" + tailrno[0] + "' and a.date_recieved = '" + new SimpleDateFormat("yyyy-MM-dd").format(jdteDate.getDate()) + "' and a.status='C' and a.flag!='D'  and b.flag!='D'  and d.flag!='D'  and e.flag!='D'";
                    populateitem("select d.item_name,sum(a.qty) from trn_stitching a left join mst_student b on a.stsl_no = b.stsl_no left join mst_item d on a.item_no = d.item_no left join mst_tailor e on a.tailor_no = e.tailor_no where a.tailor_no = '" + tailrno[0] + "' and a.date_recieved = '" + new SimpleDateFormat("yyyy-MM-dd").format(jdteDate.getDate()) + "' and a.status='C' and a.flag!='D'  and b.flag!='D'  and d.flag!='D'  and e.flag!='D' group by a.item_no");
                }
            }
            rs = stmt.executeQuery(query);
            while (rs.next()) {
                temp = model1.getRowCount();
                model1.addRow(new Object[]{});
                jtblStitchingMaster.setValueAt(rs.getString(1), temp, 0);
                jtblStitchingMaster.setValueAt(rs.getString(2), temp, 1);
                jtblStitchingMaster.setValueAt(rs.getString(3), temp, 2);
                jtblStitchingMaster.setValueAt(rs.getString(4), temp, 3);
                jtblStitchingMaster.setValueAt(rs.getString(5), temp, 4);
                jtblStitchingMaster.setValueAt(rs.getString(6), temp, 5);
                jtblStitchingMaster.setValueAt(rs.getString(7), temp, 6);
                jtblStitchingMaster.setValueAt(rs.getString(8), temp, 7);
                jtblStitchingMaster.setValueAt(rs.getString(9), temp, 8);
            }
            if (jcmbStatus.getSelectedItem().equals("Pending") && jtblStitchingMaster.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "There are no pending items with stitching master");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } finally {
            try {
                if (!rs.isClosed()) {
                    rs.close();
                }
                if (!con.isClosed()) {
                    con.close();
                }
                if (!stmt.isClosed()) {
                    stmt.isClosed();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }

    private void populateitem(String q) {
        try {
            rowcount = model2.getRowCount();
            for (int i = 0; i < rowcount; i++) {
                model2.removeRow(0);
            }
            rs = stmt.executeQuery(q);
            while (rs.next()) {
                model2.addRow(new Object[]{});
                jtblItem.setValueAt(rs.getString(1), model2.getRowCount() - 1, 0);
                jtblItem.setValueAt(rs.getString(2), model2.getRowCount() - 1, 1);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
