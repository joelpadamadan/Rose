    private void populatetable() {
        try {
            rowcount = 0;
            con = new DBConnection().connect();
            stmt = con.createStatement();
            rowcount = jtblCuttingMaster.getRowCount();
            for (int i = 0; i < rowcount; i++) {
                model1.removeRow(0);
            }
            query = "select cutting_master from mst_group where flag != 'D' and year(date_created)='" + Global.year + "' order by group_no desc";
            rs = stmt.executeQuery(query);
            while (rs.next()) {
                rowcount = jtblCuttingMaster.getRowCount();
                model1.addRow(new Object[]{""});
                jtblCuttingMaster.setValueAt(rs.getString(1), rowcount, 0);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } finally {
            try {
                if (!rs.isClosed()) {
                    rs.close();
                }
                if (!con.isClosed()) {
                    con.close();
                }
                if (!stmt.isClosed()) {
                    stmt.isClosed();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }

    }
    private void jbtnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSubmitActionPerformed
        try {
            if (jtxtCuttingMaster.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Please enter cutting master name to proceed", "Info", JOptionPane.INFORMATION_MESSAGE);
            } else {
                int i = 0;
                con = new DBConnection().connect();
                con.setAutoCommit(false);
                stmt = con.createStatement();
                query = "select group_no from mst_group where cutting_master = '" + jtxtCuttingMaster.getText() + "' and flag!='D'";
                rs = stmt.executeQuery(query);
                if (!rs.next()) {
                    query = "insert into mst_group(cutting_master,flag,date_created) values('" + jtxtCuttingMaster.getText() + "','I','" + new SimpleDateFormat("yyyy-MM-dd").format(new Date()) + "')";
                    i = stmt.executeUpdate(query);
                } else {
                    JOptionPane.showMessageDialog(this, "This name already exist!", "Info", JOptionPane.INFORMATION_MESSAGE);
                }
                if (i > 0) {
                    con.commit();
                    JOptionPane.showMessageDialog(this, "Added Successfully", "Info", JOptionPane.INFORMATION_MESSAGE);
                    jtxtCuttingMaster.grabFocus();
                    populatetable();
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } finally {
            try {
                if (!rs.isClosed()) {
                    rs.close();
                }
                if (!con.isClosed()) {
                    con.close();
                }
                if (!stmt.isClosed()) {
                    stmt.isClosed();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jbtnSubmitActionPerformed
