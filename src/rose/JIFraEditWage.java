    private void jbtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSaveActionPerformed
        if (slno == 0) {
            JOptionPane.showMessageDialog(this, "Please select a record to update", "Select", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtxtAmount.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter an amount", "Info", JOptionPane.INFORMATION_MESSAGE);
        } else if (jdteDate.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Please enter a date", "Info", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtxtDescription.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter a description", "Info", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                con = new DBConnection().connect();
                stmt = con.createStatement();
                query = "UPDATE mst_wage SET DESCRIPTION = '" + jtxtDescription.getText().trim() + "',AMOUNT ='" + jtxtAmount.getText().trim() + "', DATE_GIVEN ='" + new SimpleDateFormat("yyyy-MM-dd").format(jdteDate.getDate()) + "', FLAG='U',DATE_UPDATED='" + today + "' WHERE SL_NO='" + slno + "'";
                int i = stmt.executeUpdate(query);
                if (i > 0) {
                    JOptionPane.showMessageDialog(this, "Update successfull!", "Info", JOptionPane.INFORMATION_MESSAGE);
                    slno = 0;
                    if (jcmbType.getSelectedItem().equals("Cutting") && !jcmbName.getSelectedItem().equals("")) {
                        populatecutting();
                    } else if (jcmbType.getSelectedItem().equals("Stitching") && !jcmbName.getSelectedItem().equals("")) {
                        populatestitching();
                    }
                    jtxtAmount.setText("");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            } finally {
                try {
                    if (!con.isClosed()) {
                        con.close();
                    }
                    if (!stmt.isClosed()) {
                        stmt.isClosed();
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jbtnSaveActionPerformed

    private void jbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDeleteActionPerformed
        if (slno != 0) {
            JOptionPane.showMessageDialog(this, "Please complete the update before deleting", "Select", JOptionPane.INFORMATION_MESSAGE);
        } else if (jtblWageAdvance.getSelectedRow() > -1) {
            try {
                int b = JOptionPane.showConfirmDialog(this, "Do you want delete the record?", "Confirm", JOptionPane.YES_NO_OPTION);
                if (b == JOptionPane.YES_OPTION) {
                    slno = Integer.parseInt(jtblWageAdvance.getValueAt(jtblWageAdvance.getSelectedRow(), 0).toString());
                    con = new DBConnection().connect();
                    stmt = con.createStatement();
                    query = "UPDATE mst_wage SET FLAG='D',DATE_UPDATED='" + today + "' WHERE SL_NO='" + slno + "'";
                    int i = stmt.executeUpdate(query);
                    if (i > 0) {
                        JOptionPane.showMessageDialog(this, "Delete successfull!", "Info", JOptionPane.INFORMATION_MESSAGE);
                        if (jcmbType.getSelectedItem().equals("Cutting") && !jcmbName.getSelectedItem().equals("")) {
                            populatecutting();
                        } else if (jcmbType.getSelectedItem().equals("Stitching") && !jcmbName.getSelectedItem().equals("")) {
                            populatestitching();
                        }
                        jtxtAmount.setText("");
                        jtxtDescription.setText("");
                    }
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            } finally {
                try {
                    if (!con.isClosed()) {
                        con.close();
                    }
                    if (!stmt.isClosed()) {
                        stmt.isClosed();
                    }
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jbtnDeleteActionPerformed

