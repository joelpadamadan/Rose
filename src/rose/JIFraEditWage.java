    private void populatestitching() {
        try {
            slno = 0;
            LabourerNo = jcmbName.getSelectedItem().toString().split("   ");
            con = new DBConnection().connect();
            con.setAutoCommit(false);
            stmt = con.createStatement();
            rowcount = jtblWageAdvance.getRowCount();
            for (int i = 0; i < rowcount; i++) {
                model.removeRow(0);
            }
            int j = 0;
            query = "select a.sl_no,a.labour_type,b.tailor_name,a.description,a.date_given,a.amount from mst_wage a join mst_tailor b on a.labour_no = b.tailor_no  where a.labour_type= 'Stitching' and b.tailor_no = '" + LabourerNo[0] + "' and a.flag!='D' and b.flag!='D' order by a.sl_no desc";
            rs = stmt.executeQuery(query);
            for (j = 0; rs.next(); j++) {
                model.addRow(new Object[]{""});
                jtblWageAdvance.setValueAt(rs.getString(1), j, 0);
                jtblWageAdvance.setValueAt(rs.getString(2), j, 1);
                jtblWageAdvance.setValueAt(rs.getString(3), j, 2);
                jtblWageAdvance.setValueAt(rs.getString(4), j, 3);
                jtblWageAdvance.setValueAt(rs.getString(5), j, 4);
                jtblWageAdvance.setValueAt(rs.getString(6), j, 5);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } finally {
            try {
                if (!rs.isClosed()) {
                    rs.close();
                }
                if (!con.isClosed()) {
                    con.close();
                }
                if (!stmt.isClosed()) {
                    stmt.isClosed();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }

